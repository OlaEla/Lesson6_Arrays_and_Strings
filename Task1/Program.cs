// Задача 1: 
// Задайте двумерный массив символов (тип char [,]). 
// Создать строку из символов этого массива.



using System;

class Program
{
    static void Main()
    {
        // Задаем двумерный массив символов
        char[,] charArray = {
            {'H', 'e', 'l', 'l', 'o'},
            {'W', 'o', 'r', 'l', 'd'}
        };

        // Создаем строку из символов массива
        string resultString = ConvertCharArrayToString(charArray);

        // Выводим результат на экран
        Console.WriteLine(resultString);
    }

    // Метод для преобразования двумерного массива символов в строку
    static string ConvertCharArrayToString(char[,] charArray) //вызываем метод ConvertCharArrayToString и передаем ему 
                                                             //аргумент charArray, который является двумерным массивом символов, 
                                                            //созданным на предыдущем этапе
    {
        // Получаем количество строк и столбцов в массиве
        int rows = charArray.GetLength(0);
        int cols = charArray.GetLength(1);

        // Создаем пустую строку, в которую будем добавлять символы
        string result = "";

        // Вложенные циклы проходят по каждому элементу массива
        for (int i = 0; i < rows; i++)
        {
            for (int j = 0; j < cols; j++)
            {
                // Добавляем текущий символ к результату
                result += charArray[i, j];  //циклы проходят по каждому элементу массива, добавляя символы к строке result.
                //Использование += в данном контексте удобно для построения строк, так как он 
                //позволяет добавлять символы к строке по мере прохождения циклов, избегая 
                //необходимости создания новых строк на каждой итерации.
            }
        }

        // Возвращаем полученную строку
        return result;
    }
}
